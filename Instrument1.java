abstract class Instrument1 {
	abstract String play();
}	
	class piano extends Instrument1{
		public String play(){
			return "Piano is playing tan tan tan tan";			
	}
	}
		class flute extends Instrument1{
			public String play()
			{
				return "Flute is playing toot toot toot toot";
			}
		}
		class guitar extends Instrument1{
			public String play()
			{
				return "Guitar is playing tin tin tin";
			}
		}
